package com.example.paycellwebclient.payment.model;

public class Logo {

    /**
     * Logo name
     */
    private String name;

    /**
     * Logo url
     */
    private String url;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
